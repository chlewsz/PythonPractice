travel_citys = ['qingdao', 'beijing', 'sichuan', 'shanghai']
print('initial list:')
print(travel_citys)

print('sorted list:')
print(sorted(travel_citys))
print(travel_citys)

print('sorted and reverse list:')
print(sorted(travel_citys, reverse=True))
print(travel_citys)

travel_citys.reverse()
print('reverse list:')
print(travel_citys)

travel_citys.reverse()
print('initial list:')
print(travel_citys)

travel_citys.sort()
print('sort list:')
print(travel_citys)

travel_citys.sort(reverse=True)
print('sort and reverse list:')
print(travel_citys)
